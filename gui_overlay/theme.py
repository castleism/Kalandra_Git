# 3d. FRAME WINDOW GEOMETRY (Christian's Photoshop assets, 2026-07-03)
# ---------------------------------------------------------------------------
# assets/Window.png: square celtic-gold frame, glass inside. Border measured
# programmatically: L140 R145 T128 B137 on a 1461x1490 canvas -> nine-slice
# with a 150px source border. Buttons are separate art (Close.png red gem,
# Minimize.png green gem, Maximize.png blue gem) drawn top-right in the glass.

FRAME_ART_FILE = "Window.png"
FRAME_BTN_FILES = {"close": "Close.png", "minimize": "Minimize.png",
                   "maximize": "Maximize.png"}
FRAME_SRC_BORDER = 150      # px of border in the SOURCE art (nine-slice cut)
FRAME_DST_BORDER = 44       # px the border renders at on screen
FRAME_EDGE = 10             # outermost px that act as resize grips
FRAME_BTN_SIZE = 38         # rendered medallion button diameter
FRAME_BTN_GAP = 8
FRAME_TITLE_STRIP = 46      # glass strip under the top border (title + buttons)


def frame_layout(w, h, border=FRAME_DST_BORDER, btn=FRAME_BTN_SIZE,
                 gap=FRAME_BTN_GAP):
    """Layout for a w×h frame window. Pure math.

    The medallion buttons OVERLAY the top gold border (like a normal
    window's title-bar buttons), vertically centered on the border band and
    right-aligned. The whole glass below the border belongs to content.

    Returns dict:
      content -> (x, y, width, height)          the glass, minus padding
      buttons -> {name: (cx, cy, r)}            minimize, maximize, close (L→R)
      title   -> (x, y)                         title text anchor (on the border)
      strip_h -> bottom of the drag strip (== border)
    """
    w = max(1, int(w)); h = max(1, int(h))
    r = btn / 2.0
    cy = border / 2.0                       # centered ON the border band
    cx_close = w - 14 - r                   # tucked to the top-right corner
    cx_max = cx_close - btn - gap
    cx_min = cx_max - btn - gap
    buttons = {"minimize": (cx_min, cy, r), "maximize": (cx_max, cy, r),
               "close": (cx_close, cy, r)}
    pad = 10
    content = (border + pad, border + pad,
               max(1, w - 2 * (border + pad)),
               max(1, h - 2 * (border + pad)))
    return {"content": content, "buttons": buttons,
            "title": (border + pad, cy),
            "strip_h": border}


def frame_hit(w, h, x, y, border=FRAME_DST_BORDER, edge=FRAME_EDGE):
    """What's under (x, y) in a frame window?

    -> 'close'|'minimize'|'maximize' | an edge name from edge_hit_test
       (resize) | 'title' (move) | None (content).
    The buttons sit ON the border, so they take priority over the resize
    rim; edges win everywhere else on the rim."""
    lay = frame_layout(w, h, border=border)
    for name, (cx, cy, r) in lay["buttons"].items():
        if (x - cx) ** 2 + (y - cy) ** 2 <= (r + 3) ** 2:
            return name
    e = edge_hit_test(x, y, w, h, border=edge)
    if e:
        return e
    cx0, cy0, cw, ch = lay["content"]
    if cx0 <= x <= cx0 + cw and cy0 <= y <= cy0 + ch:
        return None
    return "title"    # anywhere on the gold border = drag


