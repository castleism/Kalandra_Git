def _hex_ok(c):
    return (isinstance(c, str) and c.startswith("#")
            and len(c) in (4, 7, 9)
            and all(ch in "0123456789abcdefABCDEF" for ch in c[1:]))


def self_check():
    """Cheap invariants: all palette entries are valid hex; QSS is balanced."""
    palette = [GOLD, GOLD_LIGHT, GOLD_DARK, GOLD_GLOW, BG0, BG1, BG2, BG3,
               TEXT, MUTED, FAINT, TEAL, COLOR_WARN]
    palette += [c for pair in GEMS.values() for c in pair]
    palette += list(ORB_TINTS.values())
    bad = [c for c in palette if not _hex_ok(c)]
    qss = kalandra_qss()
    problems = []
    if bad:
        problems.append(f"bad hex: {bad}")
    if qss.count("{") != qss.count("}"):
        problems.append("QSS braces unbalanced")
    if "qlineargradient" not in qss:
        problems.append("QSS lost its gradients")
    for token in ("QPushButton", "QTabBar::tab:selected", "QCheckBox::indicator",
                  "QScrollBar", "QComboBox QAbstractItemView", "QToolTip"):
        if token not in qss:
            problems.append(f"QSS missing rule for {token}")
    return problems
