def apply(widget, extra=""):
    """Apply the full Kalandra theme to a widget/dialog (+ optional extras)."""
    widget.setStyleSheet(kalandra_qss() + ("\n" + extra if extra else ""))


def set_gem(button, gem):
    """Tag a QPushButton with a gem accent and refresh its style."""
    button.setProperty("gem", gem if gem in GEMS else "diamond")
    st = button.style()
    st.unpolish(button)
    st.polish(button)


