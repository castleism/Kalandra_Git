# 3c. MIRROR WINDOW GEOMETRY (pure math; measured from the actual art)
# ---------------------------------------------------------------------------
# The window art is assets/mirror_frame_square.png — the original mirror
# warped into a squircle (squarer glass, gothic pendant shortened to 45%),
# generated 2026-07-03 per Christian's request. Fractions are relative to
# the CROP below and were computed from the warp's forward map, not guessed.
# (The untouched mirror_of_kalandra.png remains the overlay's own art.)

MIRROR_ART_FILE = "mirror_frame_square.png"   # falls back to the original
MIRROR_ART_CROP = (34, 202, 1871, 1397)       # x, y, w, h on the 1920 canvas
MIRROR_ASPECT = MIRROR_ART_CROP[2] / MIRROR_ART_CROP[3]   # ~1.339 (w / h)

# Face medallions on the top arc (window buttons). Gem colors are IN the art:
# red face = close, blue face = minimize, green face = maximize.
MIRROR_BUTTONS = {
    "close":    (0.1931, 0.2903),   # red-gem face, left
    "minimize": (0.4934, 0.1037),   # blue-gem face, top center
    "maximize": (0.7967, 0.2903),   # green-gem face, right
}
MIRROR_BUTTON_RADIUS = 0.0864       # fraction of window WIDTH

# The star rosette at bottom-right doubles as the resize grip.
MIRROR_RESIZE_GRIP = (0.9297, 0.7036)
MIRROR_GRIP_RADIUS = 0.1127

# Rectangle inscribed in the glass where content lives: below the side
# medallions (so widgets never cover the gold), inside the squircle.
MIRROR_CONTENT = (0.2166, 0.4073, 0.7733, 0.7573)   # x0, y0, x1, y1 fractions

# Where the window title paints (centered), in the open glass between the
# side medallions, just above the content area.
MIRROR_TITLE_Y = 0.330


def mirror_layout(w, h):
    """All hit/layout geometry for a w×h mirror window. Pure math.

    Returns dict with pixel values:
      content -> (x, y, width, height)
      buttons -> {name: (cx, cy, r)}
      grip    -> (cx, cy, r)
      title_y -> int
    """
    w = max(1, int(w)); h = max(1, int(h))
    x0, y0, x1, y1 = MIRROR_CONTENT
    content = (int(w * x0), int(h * y0),
               max(1, int(w * (x1 - x0))), max(1, int(h * (y1 - y0))))
    br = max(6.0, w * MIRROR_BUTTON_RADIUS)
    buttons = {name: (w * fx, h * fy, br)
               for name, (fx, fy) in MIRROR_BUTTONS.items()}
    gx, gy = MIRROR_RESIZE_GRIP
    grip = (w * gx, h * gy, max(8.0, w * MIRROR_GRIP_RADIUS))
    return {"content": content, "buttons": buttons, "grip": grip,
            "title_y": int(h * MIRROR_TITLE_Y)}


def mirror_hit(w, h, x, y):
    """What's under (x, y)? -> 'close'|'minimize'|'maximize'|'grip'|None.

    The CONTENT AREA always wins: the side medallions bleed into the glass
    corners, and a stray click inside your content must never close the
    window. The outer half of each medallion (including its center) remains
    clickable."""
    lay = mirror_layout(w, h)
    cx0, cy0, cw, ch = lay["content"]
    if cx0 <= x <= cx0 + cw and cy0 <= y <= cy0 + ch:
        return None
    for name, (cx, cy, r) in lay["buttons"].items():
        if (x - cx) ** 2 + (y - cy) ** 2 <= r * r:
            return name
    cx, cy, r = lay["grip"]
    if (x - cx) ** 2 + (y - cy) ** 2 <= r * r:
        return "grip"
    return None


