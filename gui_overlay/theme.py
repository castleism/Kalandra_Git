"""
GUI_OVERLAY/THEME.PY  --  The Mirror of Kalandra look, in one place. (W3-01)

Single source of truth for the app's visual identity: the gold-leaf palette,
gem accents (ruby / sapphire / emerald / diamond), the shared Qt stylesheet,
and painter helpers for the ornate golden frame and cut-gem shapes.

Design rules:
  * Palette + stylesheet generation are PURE PYTHON — importable with no PyQt
    installed (so tests and headless tools can audit them).
  * Anything that paints imports PyQt lazily inside the function.
  * Every window/dialog in the app should call `theme.apply(widget)` (or embed
    `kalandra_qss()` in its stylesheet) instead of defining local colors.
  * Missing art never breaks anything: painter helpers draw procedural gold
    and gems; the future asset kit (W3-03) only *upgrades* the look.

Usage:
    from gui_overlay import theme
    theme.apply(dialog)                        # full themed stylesheet
    theme.apply(dialog, extra="QLabel{...}")   # + widget-specific rules

    # Gem-accented buttons (ruby=danger/close, emerald=confirm, sapphire=info,
    # diamond=neutral highlight):
    btn.setProperty("gem", "ruby"); btn.style().unpolish(btn); btn.style().polish(btn)

    # Custom paintEvent chrome (W3-02 KalandraWindow uses this):
    theme.paint_ornate_frame(painter, self.rect())
"""

