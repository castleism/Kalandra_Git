# 3. PAINTER HELPERS (lazy PyQt) — ornate frame + cut gems
# ---------------------------------------------------------------------------

def _qt():
    """Lazy PyQt import so this module stays importable headless."""
    from PyQt6.QtCore import Qt, QRectF, QPointF
    from PyQt6.QtGui import (QColor, QPen, QBrush, QLinearGradient,
                             QRadialGradient, QPainterPath, QPolygonF)
    return Qt, QRectF, QPointF, QColor, QPen, QBrush, QLinearGradient, \
        QRadialGradient, QPainterPath, QPolygonF


def paint_ornate_frame(painter, rect, radius=12, gems=True, band=3):
    """Draw the layered golden frame inside `rect`.

    Layers (outside -> in): obsidian edge, gold gradient band, bright inner
    hairline. Optionally a cut gem in each corner (ruby TL, sapphire TR,
    emerald BL, diamond BR — the medallion order).
    Call from a paintEvent with antialiasing on.
    """
    (Qt, QRectF, QPointF, QColor, QPen, QBrush, QLinearGradient,
     QRadialGradient, QPainterPath, QPolygonF) = _qt()

    r = QRectF(rect).adjusted(1, 1, -1, -1)

    # 1. obsidian edge
    painter.setPen(QPen(QColor("#05060a"), 2))
    painter.setBrush(Qt.BrushStyle.NoBrush)
    painter.drawRoundedRect(r, radius, radius)

    # 2. gold band (vertical sheen: light top -> dark bottom, like leafed metal)
    g = QLinearGradient(r.topLeft(), r.bottomLeft())
    g.setColorAt(0.0, QColor(GOLD_LIGHT))
    g.setColorAt(0.25, QColor(GOLD))
    g.setColorAt(0.6, QColor(GOLD_DARK))
    g.setColorAt(1.0, QColor(GOLD))
    inner = r.adjusted(band, band, -band, -band)
    painter.setPen(QPen(QBrush(g), band))
    painter.drawRoundedRect(inner, radius - 2, radius - 2)

    # 3. inner hairline
    painter.setPen(QPen(QColor(GOLD_GLOW), 1))
    hl = inner.adjusted(band - 1, band - 1, -(band - 1), -(band - 1))
    painter.drawRoundedRect(hl, radius - 3, radius - 3)

    if gems:
        gr = max(4.0, min(r.width(), r.height()) * 0.018 + 3)
        inset = radius + 2
        corners = [
            ("ruby",     QPointF(r.left() + inset,  r.top() + inset)),
            ("sapphire", QPointF(r.right() - inset, r.top() + inset)),
            ("emerald",  QPointF(r.left() + inset,  r.bottom() - inset)),
            ("diamond",  QPointF(r.right() - inset, r.bottom() - inset)),
        ]
        for name, center in corners:
            draw_gem(painter, center, gr, name)


def draw_gem(painter, center, radius, gem="ruby"):
    """A cut gem: gold bezel, radial-lit body, specular sparkle."""
    (Qt, QRectF, QPointF, QColor, QPen, QBrush, QLinearGradient,
     QRadialGradient, QPainterPath, QPolygonF) = _qt()

    base_hex, light_hex = GEMS.get(gem, GEMS["ruby"])

    # gold bezel ring
    painter.setPen(QPen(QColor(GOLD_DARK), 1.5))
    painter.setBrush(QBrush(QColor(GOLD)))
    painter.drawEllipse(center, radius + 2.5, radius + 2.5)

    # gem body, lit from upper-left
    rg = QRadialGradient(
        QPointF(center.x() - radius * 0.4, center.y() - radius * 0.4),
        radius * 2.1)
    rg.setColorAt(0.0, QColor(light_hex))
    rg.setColorAt(0.55, QColor(base_hex))
    rg.setColorAt(1.0, QColor(base_hex).darker(180))
    painter.setPen(QPen(QColor(base_hex).darker(200), 1))
    painter.setBrush(QBrush(rg))
    painter.drawEllipse(center, radius, radius)

    # facet line + sparkle
    painter.setPen(QPen(QColor(255, 255, 255, 70), 1))
    painter.drawEllipse(center, radius * 0.55, radius * 0.55)
    painter.setPen(Qt.PenStyle.NoPen)
    painter.setBrush(QBrush(QColor(255, 255, 255, 200)))
    painter.drawEllipse(
        QPointF(center.x() - radius * 0.35, center.y() - radius * 0.4),
        radius * 0.18, radius * 0.18)


def draw_orb_dot(painter, center, radius, slug="divine"):
    """Small procedural currency-orb dot (until the W3-03 art kit lands)."""
    (Qt, QRectF, QPointF, QColor, QPen, QBrush, QLinearGradient,
     QRadialGradient, QPainterPath, QPolygonF) = _qt()
    tint = ORB_TINTS.get(slug, ORB_TINTS["divine"])
    rg = QRadialGradient(
        QPointF(center.x() - radius * 0.3, center.y() - radius * 0.35),
        radius * 2.0)
    rg.setColorAt(0.0, QColor("#ffffff"))
    rg.setColorAt(0.35, QColor(tint))
    rg.setColorAt(1.0, QColor(tint).darker(220))
    painter.setPen(QPen(QColor(tint).darker(250), 1))
    painter.setBrush(QBrush(rg))
    painter.drawEllipse(center, radius, radius)


