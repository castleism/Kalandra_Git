def edge_hit_test(x, y, w, h, border=6, corner=14):
    """Which resize edge is under (x, y) in a w×h frameless window?

    Returns one of: 'left','right','top','bottom','topleft','topright',
    'bottomleft','bottomright', or None (not on an edge). Corners use a
    larger zone so diagonal grabs are easy. Pure math — no Qt.
    """
    if w <= 0 or h <= 0 or x < 0 or y < 0 or x > w or y > h:
        return None
    on_l = x <= border
    on_r = x >= w - border
    on_t = y <= border
    on_b = y >= h - border
    # corner zones (bigger targets)
    c_l = x <= corner
    c_r = x >= w - corner
    c_t = y <= corner
    c_b = y >= h - corner
    if (on_t and c_l) or (on_l and c_t):
        return "topleft"
    if (on_t and c_r) or (on_r and c_t):
        return "topright"
    if (on_b and c_l) or (on_l and c_b):
        return "bottomleft"
    if (on_b and c_r) or (on_r and c_b):
        return "bottomright"
    if on_l:
        return "left"
    if on_r:
        return "right"
    if on_t:
        return "top"
    if on_b:
        return "bottom"
    return None


